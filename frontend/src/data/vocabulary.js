// Vocabulary database — Goethe/telc standard
// Class-based for reusability

class Word {
  constructor(w, t, g = null, ex = null) {
    this.word = w;
    this.translation = t;
    this.gender = g; // m, f, n, or null for verbs/adj
    this.example = ex;
  }
  get genderLabel() {
    return { m: 'der (masculine)', f: 'die (feminine)', n: 'das (neuter)' }[this.gender] || '';
  }
}

class VocabLevel {
  constructor(level, words) {
    this.level = level;
    this.words = words;
  }
  get count() { return this.words.length; }
  random(n = 10) { return [...this.words].sort(() => Math.random() - 0.5).slice(0, n); }
  byGender(g) { return this.words.filter(w => w.gender === g); }
}

